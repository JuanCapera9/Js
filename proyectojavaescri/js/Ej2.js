//Area y perimetro del cuadrado

let ladoCuadrado = 8;

// Área del cuadrado: lado * lado
let areaCuadrado = ladoCuadrado * ladoCuadrado;

// Perímetro del cuadrado: 4 * lado
let perimetroCuadrado = 4 * ladoCuadrado;

// Mostrar resultados con ventanas emergentes
alert("Área del cuadrado: " + areaCuadrado);
alert("Perímetro del cuadrado: " + perimetroCuadrado);

//Área y perímetro de un triángulo:

let baseTriangulo = 9;
let alturaTriangulo = 8;
let ladoUnoTriangulo = 8;
let ladoDosTriangulo = 8;

// Área del triángulo: (base * altura) / 2
let areaTriangulo = (baseTriangulo * alturaTriangulo) / 2;

// Perímetro del triángulo: lado1 + lado2 + base
let perimetroTriangulo = ladoUnoTriangulo + ladoDosTriangulo + baseTriangulo;

// Mostrar resultados con ventanas emergentes
alert("Área del triángulo: " + areaTriangulo);
alert("Perímetro del triángulo: " + perimetroTriangulo);

//Área y perímetro de un rectángulo:

let baseRectangulo = 8;
let alturaRectangulo = 6;

// Área del rectángulo: base * altura
let areaRectangulo = baseRectangulo * alturaRectangulo;

// Perímetro del rectángulo: 2 * (base + altura)
let perimetroRectangulo = 2 * (baseRectangulo + alturaRectangulo);

// Mostrar resultados con ventanas emergentes
alert("Área del rectángulo: " + areaRectangulo);
alert("Perímetro del rectángulo: " + perimetroRectangulo);
